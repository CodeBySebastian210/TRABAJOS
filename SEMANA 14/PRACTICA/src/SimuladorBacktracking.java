    public boolean resolverLaberinto(int[][] lab, int x, int y, int[][] sol) {
        int n = lab.length;
        if (x == n - 1 && y == n - 1 && lab[x][y] == 1) {
            sol[x][y] = 1;
            areaLog.append("ðŸ Llegamos a la meta (" + x + "," + y + ")\n");
            return true;
        }

        if (esSeguro(lab, x, y)) {

            sol[x][y] = 1;
            areaLog.append("âž¡ Avanzando a (" + x + "," + y + ")\n");
            if (resolverLaberinto(lab, x + 1, y, sol)) return true;
            if (resolverLaberinto(lab, x, y + 1, sol)) return true;
            sol[x][y] = 0;
            areaLog.append("â›” Retrocediendo desde (" + x + "," + y + ")\n");
            return false;
        }

        areaLog.append("âŒ PosiciÃ³n bloqueada o invÃ¡lida: (" + x + "," + y + ")\n");
        return false;
    }
    
    public boolean esSeguro(int[][] lab, int x, int y) {
        int n = lab.length;

        return (x >= 0 && x < n &&
                y >= 0 && y < n &&
                lab[x][y] == 1);
    }
    
    public void mostrarMatriz(int[][] matriz) {
        for (int i = 0; i < matriz.length; i++) {
            for (int j = 0; j < matriz[0].length; j++) {
                areaLog.append(matriz[i][j] + " ");
            }
            areaLog.append("\n");
        }
    }
